public class KonversiNumberOverflow {

    public static void main(String[] args) {
  
      int iniInt = 128;
      byte iniByte = (byte) iniInt;
  
      System.out.println(iniByte);
  
      int iniInt2 = 129;
      byte iniByte2 = (byte) iniInt2;
  
      System.out.println(iniByte2);
  
    }
  }